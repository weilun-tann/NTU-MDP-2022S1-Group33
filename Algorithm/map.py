class Map:
    def __init__(self):
        pass

    def is_obstacle(self, x, y, sim=True):
        if sim:
            return map_sim[x][y] in [10, 11, 12, 13]  # TODO - direction encoding

    def is_free(self, x, y, sim=True):
        return not self.is_obstacle(x, y, sim)

    def valid_range(self, y, x):
        return (0 <= y < config.map_size["height"]) and (
            0 <= x < config.map_size["width"]
        )

    def is_valid_open(self, x, y):
        return map_sim[x][y] == 0

    def reset(self):
        # map_virtual = [[0 for x in range(config.map_size['width'])] for y in range(config.map_size['height'])]
        # map_is_explored = [[0 for x in range(config.map_size['width'])] for y in range(config.map_size['height'])]

        for x in range(config.map_size["width"]):
            for y in range(config.map_size["height"]):
                map_sim[y][x] = map_virtual[y][x]
                map_is_explored[y][x] = 0

        # assuming robot always start at the start position
        for i in range(5):
            for j in range(5):
                map_is_explored[config.map_size["height"] - 1 - i][j] = 1
                map_is_explored[i][config.map_size["width"] - 1 - j] = 1

    def create_map(self, obstacles):
        for x in range(config.map_size["width"]):
            for y in range(config.map_size["height"]):
                map_sim[y][x] = map_virtual_w[y][x]
        for i in range(len(obstacles)):
            for row in range(config.map_size["width"]):
                for column in range(config.map_size["height"]):
                    if row == obstacles[i][0] and column == obstacles[i][1]:
                        map_sim[column][row] = obstacles[i][2]
                        for j in range(3):
                            for k in range(3):
                                if not (
                                    obstacles[i][0] - 1 + k < 0
                                    or obstacles[i][0] + k > config.map_size["width"]
                                    or obstacles[i][1] - 1 + j < 0
                                    or obstacles[i][1] - 1 + j
                                    >= config.map_size["height"]
                                ):
                                    # Skip the obstacle itself to prevent changes
                                    if not map_sim[obstacles[i][1] - 1 + j][
                                        obstacles[i][0] - 1 + k
                                    ] in [10, 11, 12, 13]:
                                        # Update surrounding wall near the obstacle (3x3)
                                        map_sim[obstacles[i][1] - 1 + j][
                                            obstacles[i][0] - 1 + k
                                        ] = 1
        for x in range(config.map_size["width"]):
            for y in range(config.map_size["height"]):
                map_virtual[y][x] = map_sim[y][x]
